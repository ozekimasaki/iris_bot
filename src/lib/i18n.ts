function lookup(dictionary: Dictionary, key: string): string | undefined {
  let current: DictionaryValue = dictionary;
  for (const segment of key.split('.')) {
    if (typeof current === 'string' || !(segment in current)) {
      return undefined;
    }
    current = current[segment];
  }
  return typeof current === 'string' ? current : undefined;
}

export function isSupportedLocale(value: string): value is SupportedLocale {
  return value === 'en' || value === 'ja' || value === 'zh-CN';
}

export function t(locale: SupportedLocale, key: string, variables: Record<string, string | number> = {}) {
  const template = lookup(dictionaries[locale], key) ?? lookup(dictionaries[defaultLocale], key) ?? key;
  return template.replaceAll(/\{([a-zA-Z0-9_]+)\}/g, (_, token) => String(variables[token] ?? `{${token}}`));
}

export function getLanguageName(locale: SupportedLocale, targetLocale: SupportedLocale) {
  const suffix = locale === 'zh-CN' ? 'zhCN' : locale;
  return t(targetLocale, `languages.${suffix}`);
}

export function getScopeName(scope: 'admin' | 'manager' | 'forum_notice', locale: SupportedLocale) {
  const suffix = scope === 'forum_notice' ? 'forumNotice' : scope;
  return t(locale, `setup.scopes.${suffix}`);
}

export function getGrantableRoleSourceName(source: string, locale: SupportedLocale) {
  if (source === 'manual' || source === 'event') {
    return t(locale, `role.sources.${source}`);
  }

  return t(locale, 'role.sources.unknown');
}

export function getHelpSummary(locale: SupportedLocale, commandName: string, fallback: string) {
  return lookup(dictionaries[locale], `help.commands.${commandName}.summary`) ??
    lookup(dictionaries[defaultLocale], `help.commands.${commandName}.summary`) ??
    fallback;
}
